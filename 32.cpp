#include <iostream>

using namespace std;

int main() {
    int a, b, e;
    int suma;

    cout << "Introduce el valor de a: ";
    cin >> a;

    cout << "Introduce el valor de b: ";
    cin >> b;

    cout << "Introduce el valor de e: ";
    cin >> e;

    suma = a + b + e;

    cout << "La suma de " << a << ", " << b << " y " << e << " es " << suma << endl;

    return 0;
}

